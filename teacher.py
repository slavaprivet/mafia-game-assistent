"""
Учитель — объясняет почему произошла ошибка и как избежать в будущем.
"""

from ai_client import ask_code_model


async def explain_error(error_text: str, context_code: str = "") -> tuple[str, int]:
    """
    Объясняет причину ошибки простым языком.
    Возвращает (объяснение, токены).
    """
    prompt_parts = [
        "Объясни эту ошибку простым языком (без технического жаргона):",
        f"\n```\n{error_text}\n```",
    ]

    if context_code:
        prompt_parts.append(f"\nКонтекст кода:\n```\n{context_code[:2000]}\n```")

    prompt_parts.append(
        "\nОтветь в формате:\n"
        "🔍 ЧТО СЛУЧИЛОСЬ: (1-2 предложения)\n"
        "🎓 ПОЧЕМУ ЭТО ПРОИЗОШЛО: (причина)\n"
        "💡 КАК ИЗБЕЖАТЬ В БУДУЩЕМ: (совет)"
    )

    return await ask_code_model(
        "\n".join(prompt_parts),
        system_prompt=(
            "Ты дружелюбный наставник для начинающего разработчика игр. "
            "Объясняй просто, без снобизма. Отвечай на русском языке."
        )
    )


async def suggest_best_practices(code: str, language: str = "python") -> tuple[str, int]:
    """
    Анализирует код и предлагает улучшения/лучшие практики.
    """
    prompt = (
        f"Посмотри на этот {language} код и предложи улучшения:\n"
        f"```\n{code[:3000]}\n```\n\n"
        "Скажи:\n"
        "✅ Что сделано хорошо\n"
        "⚠️ Что можно улучшить (2-3 совета максимум)\n"
        "💡 Одна лучшая практика которую стоит применить"
    )

    return await ask_code_model(
        prompt,
        system_prompt=(
            "Ты ментор разработчика игр. Критикуй конструктивно, "
            "хвали за хорошее. Краткие конкретные советы. Отвечай на русском."
        )
    )
